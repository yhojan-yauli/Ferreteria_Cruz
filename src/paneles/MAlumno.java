    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
       if (txtCodAlumno.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Seleccione un alumno de la tabla.");
        return;
    }

    if (!validarCampos()) return;

    Alumno alu = new Alumno();
    alu.setCodAlumno(Integer.parseInt(txtCodAlumno.getText().trim()));
    alu.setNombres(txtNombres.getText().trim());
    alu.setApellidos(txtApellidos.getText().trim());
    alu.setDni(txtDni.getText().trim()); // no editable desde UI
    alu.setEdad(Integer.parseInt(txtEdad.getText().trim()));
    alu.setCelular(Integer.parseInt(txtCelular.getText().trim()));

    // mantener el mismo estado actual
    String estado = lblEstado2.getText();
    if (estado.equalsIgnoreCase("Registrado")) alu.setEstado(0);
    else if (estado.equalsIgnoreCase("Matriculado")) alu.setEstado(1);
    else if (estado.equalsIgnoreCase("Retirado")) alu.setEstado(2);

    if (alumnoDAO.actualizarAlumno(alu)) {
        JOptionPane.showMessageDialog(this, "Alumno modificado correctamente.");
        limpiarCampos();
        cargarDatosTabla();
    } else {
        JOptionPane.showMessageDialog(this, "Error al modificar el alumno.");
    }   

    }//GEN-LAST:event_btnModificarActionPerformed

