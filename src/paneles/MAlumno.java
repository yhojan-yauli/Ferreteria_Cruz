    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
     String dni = txtBuscar.getText().trim();

    if (dni.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese un DNI para buscar.");
        return;
    }
    if (!dni.matches("\\d{8}")) {
        JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos.");
        return;
    }

    Alumno encontrado = alumnoDAO.buscarPorDNI(dni);

    if (encontrado != null) {
        txtCodAlumno.setText(String.valueOf(encontrado.getCodAlumno()));
        txtNombres.setText(encontrado.getNombres());
        txtApellidos.setText(encontrado.getApellidos());
        txtDni.setText(encontrado.getDni());
        txtEdad.setText(String.valueOf(encontrado.getEdad()));
        txtCelular.setText(String.valueOf(encontrado.getCelular()));
        lblEstado2.setText(encontrado.getEstadoTexto());

        // seleccionar en la tabla
        for (int i = 0; i < tblAlumnos.getRowCount(); i++) {
            int cod = (int) tblAlumnos.getValueAt(i, 0);
            if (cod == encontrado.getCodAlumno()) {
                tblAlumnos.setRowSelectionInterval(i, i);
                tblAlumnos.scrollRectToVisible(tblAlumnos.getCellRect(i, 0, true));
                break;
            }
        }

        JOptionPane.showMessageDialog(this, "Alumno encontrado.");
    } else {
        JOptionPane.showMessageDialog(this, "No se encontró un alumno con ese DNI.");
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

