    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       if (txtCodAlumno.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Seleccione un alumno de la tabla.");
        return;
    }

    String estado = lblEstado2.getText();
    if (!estado.equalsIgnoreCase("Registrado")) {
        JOptionPane.showMessageDialog(this,
                "Solo se puede eliminar alumnos con estado 0 (Registrado).",
                "No permitido",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    int cod = Integer.parseInt(txtCodAlumno.getText().trim());
    int r = JOptionPane.showConfirmDialog(this,
            "¿Está seguro de eliminar este alumno?",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION);

    if (r == JOptionPane.YES_OPTION) {
        if (alumnoDAO.eliminarAlumno(cod)) {
            JOptionPane.showMessageDialog(this, "Alumno eliminado correctamente.");
            limpiarCampos();
            cargarDatosTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar el alumno.");
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblAlumnosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlumnosMouseClicked
    int fila = tblAlumnos.getSelectedRow();
    if (fila == -1) return;

    txtCodAlumno.setText(tblAlumnos.getValueAt(fila, 0).toString());
    txtNombres.setText(tblAlumnos.getValueAt(fila, 1).toString());
    txtApellidos.setText(tblAlumnos.getValueAt(fila, 2).toString());
    txtDni.setText(tblAlumnos.getValueAt(fila, 3).toString());
    txtEdad.setText(tblAlumnos.getValueAt(fila, 4).toString());
    txtCelular.setText(tblAlumnos.getValueAt(fila, 5).toString());
    lblEstado2.setText(tblAlumnos.getValueAt(fila, 6).toString());

    txtDni.setEditable(false); // 🔒 NO permitir cambiar DNI en alumnos existentes
    txtCodAlumno.setEditable(false); // por si acaso también
    }//GEN-LAST:event_tblAlumnosMouseClicked

