    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
     
 if (!validarCampos()) return;

    String dni = txtDni.getText().trim();
    if (!dni.matches("\\d{8}")) {
        JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos.");
        return;
    }

    // verificar duplicado
    Alumno existe = alumnoDAO.buscarPorDNI(dni);
    if (existe != null) {
        JOptionPane.showMessageDialog(this, "El DNI ya está registrado.");
        return;
    }

    int cod = Integer.parseInt(txtCodAlumno.getText().trim());
    Alumno alu = new Alumno();
    alu.setCodAlumno(cod);
    alu.setNombres(txtNombres.getText().trim());
    alu.setApellidos(txtApellidos.getText().trim());
    alu.setDni(dni);
    alu.setEdad(Integer.parseInt(txtEdad.getText().trim()));
    alu.setCelular(Integer.parseInt(txtCelular.getText().trim()));
    alu.setEstado(0); // 0 = Registrado

    if (alumnoDAO.insertarAlumno(alu)) {
        JOptionPane.showMessageDialog(this, "Alumno registrado correctamente.");
        limpiarCampos();
        cargarDatosTabla();
    } else {
        JOptionPane.showMessageDialog(this, "Error al registrar el alumno.");
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

