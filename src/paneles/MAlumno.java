private void configurarTabla() {
        modeloTabla = new DefaultTableModel(
                new Object[]{"Código", "Nombres", "Apellidos", "DNI", "Edad", "Celular", "Estado"},
                0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblAlumnos.setModel(modeloTabla);
        tblAlumnos.getTableHeader().setReorderingAllowed(false);
    }

    private void cargarDatosTabla() {
        modeloTabla.setRowCount(0);
        List<Alumno> alumnos = alumnoDAO.listarAlumnos();
        for (Alumno a : alumnos) {
            Object[] fila = {
                a.getCodAlumno(),
                a.getNombres(),
                a.getApellidos(),
                a.getDni(),
                a.getEdad(),
                a.getCelular(),
                a.getEstadoTexto()
            };
            modeloTabla.addRow(fila);
        }
    }
  private int generarCodigo() {
        int max = 0;
        List<Alumno> alumnos = alumnoDAO.listarAlumnos();
        for (Alumno a : alumnos) {
            if (a.getCodAlumno() > max) {
                max = a.getCodAlumno();
            }
        }
        return max + 1;
    }

    private boolean dniExiste(String dni) {
        Alumno a = alumnoDAO.buscarPorDNI(dni);
        return a != null;
    }

