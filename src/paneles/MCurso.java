    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (txtCodCurso.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Seleccione un curso de la tabla.",
                "Sin selección",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    int codCurso = Integer.parseInt(txtCodCurso.getText().trim());

    // Validar que no tenga matrículas
    if (matriculaDAO.existeMatriculaEnCurso(codCurso)) {
        JOptionPane.showMessageDialog(this,
                "No se puede eliminar: existen alumnos matriculados en este curso.",
                "Operación no permitida",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    int r = JOptionPane.showConfirmDialog(this,
            "¿Está seguro de eliminar este curso?",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION);

    if (r == JOptionPane.YES_OPTION) {
        if (cursoDAO.eliminarCurso(codCurso)) {
            JOptionPane.showMessageDialog(this,
                    "Curso eliminado correctamente.",
                    "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
            cargarCursosTabla();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Error al eliminar el curso.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

