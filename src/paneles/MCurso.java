    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
         if (!validarCampos()) return;

    int codCurso = Integer.parseInt(txtCodCurso.getText().trim());

    // Validar que el código no se repita
    Curso existente = cursoDAO.buscarPorCodigo(codCurso);
    if (existente != null) {
        JOptionPane.showMessageDialog(this,
                "Ya existe un curso con este código.",
                "Código duplicado",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    String asignatura = txtAsignatura.getText().trim();
    int ciclo = Integer.parseInt(txtCiclo.getText().trim());
    int creditos = Integer.parseInt(txtCreditos.getText().trim());
    int horas = Integer.parseInt(txtHoras.getText().trim());

    Curso curso = new Curso(codCurso, asignatura, ciclo, creditos, horas);

int resultado = cursoDAO.insertarCurso(curso);

    if (resultado == 1) { // según tu SP: 1 = éxito
        JOptionPane.showMessageDialog(this,
                "Curso registrado correctamente.",
                "Éxito",
                JOptionPane.INFORMATION_MESSAGE);
        limpiarCampos();
        cargarCursosTabla();
    } else if (resultado == -1) {
        JOptionPane.showMessageDialog(this,
                "Código ya registrado.",
                "Error",
                JOptionPane.ERROR_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this,
                "Error al registrar el curso.",
                "Error",
                JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

