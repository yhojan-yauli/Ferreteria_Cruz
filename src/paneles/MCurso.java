 private void configurarTabla() {
        modeloCursos = new DefaultTableModel(
                new Object[]{"Código", "Asignatura", "Ciclo", "Créditos", "Horas"},
                0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // no editable
            }
        };
        jTable2.setModel(modeloCursos);
    }

    private void cargarCursosTabla() {
        modeloCursos.setRowCount(0);
        List<Curso> cursos = cursoDAO.listarCursos();
        // si tu SP ya trae ORDER BY codCurso, ya queda ordenado
        for (Curso c : cursos) {
            modeloCursos.addRow(new Object[]{
                c.getCodCurso(),
                c.getAsignatura(),
                c.getCiclo(),
                c.getCreditos(),
                c.getHoras()
            });
        }
    }

    private void limpiarCampos() {
        txtCodCurso.setText("");
        txtAsignatura.setText("");
        txtCiclo.setText("");
        txtCreditos.setText("");
        txtHoras.setText("");
        jTable2.clearSelection();
        txtCodCurso.setEditable(true); // para poder escribir otro código al adicionar
    }

    private boolean validarCampos() {
        if (txtCodCurso.getText().trim().isEmpty()
                || txtAsignatura.getText().trim().isEmpty()
                || txtCiclo.getText().trim().isEmpty()
                || txtCreditos.getText().trim().isEmpty()
                || txtHoras.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Complete todos los campos.",
                    "Campos incompletos",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }
        // numéricos:
        try {
            Integer.parseInt(txtCodCurso.getText().trim());
            Integer.parseInt(txtCiclo.getText().trim());
            Integer.parseInt(txtCreditos.getText().trim());
            Integer.parseInt(txtHoras.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Código, Ciclo, Créditos y Horas deben ser números.",
                    "Formato inválido",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        // código 4 dígitos
        if (!txtCodCurso.getText().trim().matches("\\d{1,4}")) {
            JOptionPane.showMessageDialog(this,
                    "El código debe ser de 4 dígitos.",
                    "Código inválido",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
