    private void btnBuscarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCursoActionPerformed
        // TODO add your handling code here:
            String texto = txtBuscarCurso.getText().trim();

    if (texto.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Ingrese código de curso .",
                "Campo vacío",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (!texto.matches("\\d{1,4}")) {
        JOptionPane.showMessageDialog(this,
                "El código debe ser válido.",
                "Código inválido",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    int codCurso = Integer.parseInt(texto);
    Curso curso = cursoDAO.buscarPorCodigo(codCurso);

    if (curso != null) {
        txtCodCurso.setText(String.valueOf(curso.getCodCurso()));
        txtAsignatura.setText(curso.getAsignatura());
        txtCiclo.setText(String.valueOf(curso.getCiclo()));
        txtCreditos.setText(String.valueOf(curso.getCreditos()));
        txtHoras.setText(String.valueOf(curso.getHoras()));
        txtCodCurso.setEditable(false);

        // seleccionar en tabla
        for (int i = 0; i < jTable2.getRowCount(); i++) {
            int cod = (int) jTable2.getValueAt(i, 0);
            if (cod == curso.getCodCurso()) {
                jTable2.setRowSelectionInterval(i, i);
                jTable2.scrollRectToVisible(jTable2.getCellRect(i, 0, true));
                break;
            }
        }
    } else {
        JOptionPane.showMessageDialog(this,
                "No se encontró un curso con ese código.",
                "Sin resultados",
                JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnBuscarCursoActionPerformed

