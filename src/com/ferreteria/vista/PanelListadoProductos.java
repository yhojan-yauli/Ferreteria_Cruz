    private void configurarTabla() {
        // Columnas basadas en tu diseño visual
        String[] titulos = {"Código", "Categoría", "Marca/Material", "Modelo Producto", "Precio", "Stock", "Ubicación"};
        DefaultTableModel modelo = new DefaultTableModel(null, titulos) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Bloqueado para que el vendedor no edite nada
            }
        };
        tablaCatalogo.setModel(modelo);
    }

   private void cargarDatosIniciales() {
        // 1. Cargar combo de Categorías
        List<ComboBoxItem> listaCat = productoDAO.listarCategoriasCombo();
        cmbCategoria1.removeAllItems();
        cmbCategoria1.addItem("Seleccione categoría"); 
        for (ComboBoxItem item : listaCat) {
            cmbCategoria1.addItem(item.toString());
        }

        // 2. Cargar MARCAS (Usando el combo cmbMaterial1)
        cmbMaterial1.removeAllItems();
        cmbMaterial1.addItem("Seleccione marca");
        
        // Truco PRO: Extraer las marcas únicas automáticamente de tu base de datos
        List<Producto> todosLosProductos = productoDAO.listarTodos(false);
        java.util.Set<String> marcasUnicas = new java.util.HashSet<>();
        
        for (Producto p : todosLosProductos) {
            if (p.getMarca() != null && !p.getMarca().trim().isEmpty()) {
                // Guardamos la marca (la convertimos a mayúsculas para evitar duplicados como "Bosch" y "BOSCH")
                marcasUnicas.add(p.getMarca().toUpperCase()); 
            }
        }
        // Llenar el ComboBox con las marcas encontradas
        for (String marca : marcasUnicas) {
            cmbMaterial1.addItem(marca);
        }

        // 3. Llenar combo de Precio
        cmbPrecio1.removeAllItems();
        cmbPrecio1.addItem("Seleccione precio");
        cmbPrecio1.addItem("Menor a S/50");
        cmbPrecio1.addItem("S/50 a S/100");
        cmbPrecio1.addItem("Mayor a S/100");

        // 4. Llenar combo de Stock
        cmbStock1.removeAllItems();
        cmbStock1.addItem("Seleccione stock");
        cmbStock1.addItem("En Stock (>0)");
        cmbStock1.addItem("Sin Stock (0)");

        // Mostrar todos los productos al abrir
        cargarTablaTodos();
    }

    private void cargarTablaTodos() {
        llenarTabla(productoDAO.listarTodos(false)); // false = solo activos
    }

    private void llenarTabla(List<Producto> lista) {
        DefaultTableModel modelo = (DefaultTableModel) tablaCatalogo.getModel();
        modelo.setRowCount(0); // Limpiar tabla

        for (Producto p : lista) {
            Object[] fila = {
                p.getCodigo(),
                p.getIdCategoria(), // Idealmente aquí iría el nombre de la categoría cruzado con la BD
                p.getMarca(),
                p.getNombre(),
                String.format("%.2f", p.getPrecioVenta()),
                p.getStockActual(),
                p.getUbicacion()
            };
            modelo.addRow(fila);
        }
    }
