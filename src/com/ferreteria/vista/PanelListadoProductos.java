    private void btnFiltroMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltroMaterialActionPerformed
      // =========================================================
    // FILTRO DE MARCAS (Usa el botón y combo de "Material")
    // =========================================================
        int index = cmbMaterial1.getSelectedIndex();
        
        if (index > 0) { // Si eligió una marca de la lista
            // Obtenemos la marca elegida en el combo
            String marcaElegida = cmbMaterial1.getSelectedItem().toString().toLowerCase();
            
            List<Producto> todos = productoDAO.listarTodos(false);
            List<Producto> filtrados = new java.util.ArrayList<>();
            
            for (Producto p : todos) {
                // Solo miramos la columna "marca" del producto
                String marcaProducto = p.getMarca() != null ? p.getMarca().toLowerCase() : "";
                
                if (marcaProducto.equals(marcaElegida)) {
                    filtrados.add(p);
                }
            }
            llenarTabla(filtrados);
        } else {
            cargarTablaTodos();
        }
    
    }//GEN-LAST:event_btnFiltroMaterialActionPerformed

    private void FiltroStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FiltroStockActionPerformed
 int index = cmbStock1.getSelectedIndex();
        if (index > 0) {
            List<Producto> todos = productoDAO.listarTodos(false);
            List<Producto> filtrados = new java.util.ArrayList<>();
            
            for (Producto p : todos) {
                int stock = p.getStockActual(); // Usamos el stock del modelo
                
                if (index == 1 && stock > 0) { 
                    // Opción: "En Stock (>0)"
                    filtrados.add(p);
                } else if (index == 2 && stock == 0) { 
                    // Opción: "Sin Stock (0)"
                    filtrados.add(p);
                }
            }
            llenarTabla(filtrados);
        } else {
            cargarTablaTodos();
        }
    }//GEN-LAST:event_FiltroStockActionPerformed

    private void btnFiltroPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltroPrecioActionPerformed
        // TODO add your handling code here:
        int index = cmbPrecio1.getSelectedIndex();
        if (index > 0) {
            List<Producto> todos = productoDAO.listarTodos(false);
            List<Producto> filtrados = new java.util.ArrayList<>();
            
            for (Producto p : todos) {
                double precio = p.getPrecioVenta(); // Usamos el precio del modelo
                
                if (index == 1 && precio < 50.0) { 
                    // Opción: "Menor a S/50"
                    filtrados.add(p);
                } else if (index == 2 && precio >= 50.0 && precio <= 100.0) { 
                    // Opción: "S/50 a S/100"
                    filtrados.add(p);
                } else if (index == 3 && precio > 100.0) { 
                    // Opción: "Mayor a S/100"
                    filtrados.add(p);
                }
            }
            llenarTabla(filtrados);
        } else {
            cargarTablaTodos();
        }
    }//GEN-LAST:event_btnFiltroPrecioActionPerformed


